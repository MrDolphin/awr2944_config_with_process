% =========================================================================
% 点云与目标关联函数
% =========================================================================
% 功能说明：
%   该函数将检测到的点云与现有跟踪目标进行关联，为每个点云找到最合适的目标
%   使用马氏距离和门限技术进行关联决策
% 
% 输入参数：
%   inst - 跟踪器实例，包含目标状态和点云数据
% 
% 输出参数：
%   inst - 更新后的跟踪器实例，点云的 best_ind 和 best_score 字段被更新
% 
% 关联过程：
%   1. 为每个目标计算门限值
%   2. 计算目标的协方差矩阵的对数行列式
%   3. 对每个点云，计算与每个目标的马氏距离
%   4. 如果马氏距离小于门限值，计算关联分数
%   5. 为每个点云选择分数最低的目标作为最佳关联
% =========================================================================

function inst = associate(inst)
    % 遍历每个跟踪目标
    for it = 1:inst.target_num_all
        
        % =================================================================
        % 1. 计算门限值
        % =================================================================
        limits = inst.gatingParams.limits;  % 门限限制参数
        volume = inst.gatingParams.volume;  % 门限体积参数
        % 计算目标的门限值
        inst.target(it).G = gateCreateLim(volume, inst.target(it).gC_inv, inst.target(it).Hs(1), limits);
        
        % =================================================================
        % 2. 计算协方差矩阵的对数行列式
        % =================================================================
        m_temp = inst.target(it).gC;  % 获取目标的协方差矩阵
        % 重塑协方差矩阵
        m_temp = [m_temp(1:3); m_temp(4:6); m_temp(7:9)];
        % 计算对数行列式，用于后续的关联分数计算
        log_det = log(det(m_temp));
        
        % =================================================================
        % 3. 遍历每个点云，计算关联分数
        % =================================================================
        for ip = 1:inst.num_points
            % 计算残差向量（点云与目标预测状态的差异）
            u_tilda(1) = inst.point_cloud(ip).range - inst.target(it).Hs(1);  % 距离残差
            u_tilda(2) = inst.point_cloud(ip).angle - inst.target(it).Hs(2);  % 角度残差
            rv_out = inst.point_cloud(ip).vel;  % 点云速度
            u_tilda(3) = rv_out - inst.target(it).Hs(3);  % 速度残差
            
            % 注释掉的速度处理代码
            % if inst.target(it).vel_handing < inst.velParams.vel_locked
            %     rv_out = unroll_radvel(inst.velParams.maxradvel,inst.target(it).allocationvel,inst.point_cloud(ip).vel);
            %     rv_out = inst.point_cloud(ip).vel;
            %     u_tilda(3) = rv_out - inst.target(it).allocationvel;
            % else
            %     rv_out = unroll_radvel(inst.velParams.maxradvel,inst.target(it).Hs(3),inst.point_cloud(ip).vel);
            %     rv_out = inst.point_cloud(ip).vel;
            %     u_tilda(3) = rv_out - inst.target(it).Hs(3);
            % end
            
            % =================================================================
            % 4. 计算马氏距离
            % =================================================================
            chi2 = computeMahalanobis(u_tilda, inst.target(it).gC_inv);
            
            % =================================================================
            % 5. 关联决策
            % =================================================================
            % 如果马氏距离小于门限值，认为点云可能属于该目标
            if(chi2 < inst.target(it).G)
                % 计算关联分数（对数行列式 + 马氏距离）
                score = log_det + chi2;
                % 如果当前分数比点云的最佳分数更低，更新最佳关联
                if(score < inst.point_cloud(ip).best_score)
                    inst.point_cloud(ip).best_score = score;  % 更新最佳分数
                    inst.point_cloud(ip).best_ind = it;  % 更新最佳关联目标索引
                    inst.point_cloud(ip).vel = rv_out;  % 更新点云速度
                end
            end
        end
    end
end

% =========================================================================
% 计算已跟踪目标的门限值函数
% =========================================================================
% 功能说明：
%   该函数计算目标的关联门限值，用于判断点云是否可能属于该目标
%   考虑了距离、角度和速度三个维度的门限限制
% 
% 输入参数：
%   volume - 门限体积参数
%   EC - 协方差矩阵的逆
%   range - 目标距离
%   gateLim - 门限限制参数 [距离门限, 角度门限, 速度门限]
% 
% 输出参数：
%   G - 计算得到的门限值
% =========================================================================

function G = gateCreateLim(volume, EC, range, gateLim)
    % 计算门限相关参数
    a = 1/sqrt(EC(1));  % 距离维度的缩放因子
    b = 1/sqrt(EC(5));  % 角度维度的缩放因子
    c = 1/sqrt(EC(9));  % 速度维度的缩放因子
    v = 4*pi/3*a*b*c;  % 计算体积
    
    % 计算门限常数
    gConst = (volume/v)^(2/3);
    
    % 计算变换矩阵
    m_temp = [EC(1:3); EC(4:6); EC(7:9)];
    LQ = chol(m_temp);  % Cholesky 分解
    W_temp = inv(LQ);  % 计算逆矩阵
    W = [W_temp(1,:) W_temp(2,:) W_temp(3,:)];  % 组合变换矩阵
    
    % 初始化门限值为 gConst
    gMin = gConst;
    
    % =================================================================
    % 应用距离门限限制
    % =================================================================
    if(gateLim(1) ~= 0)
        % 计算距离门限对应的 gLimit
        gLimit = gateLim(1)^2 / (4 * (W(1)^2 + W(4)^2 + W(7)^2));
        % 如果当前 gMin 大于 gLimit，更新 gMin
        if(gMin > gLimit)
            gMin = gLimit;
        end
    end
    
    % =================================================================
    % 应用角度门限限制
    % =================================================================
    if(gateLim(2) ~= 0)
        % 计算角度对应的宽度
        sWidth = 2 * range * tan(sqrt(W(5)^2 + W(8)^2));
        % 计算角度门限对应的 gLimit
        gLimit = gateLim(2)^2 / (sWidth^2);
        % 如果当前 gMin 大于 gLimit，更新 gMin
        if(gMin > gLimit)
            gMin = gLimit;
        end
    end
    
    % =================================================================
    % 应用速度门限限制
    % =================================================================
    if(gateLim(3) ~= 0)
        % 计算速度门限对应的 gLimit
        gLimit = gateLim(3)^2 / (4 * (W(9)^2));
        % 如果当前 gMin 大于 gLimit，更新 gMin
        if(gMin > gLimit)
            gMin = gLimit;
        end
    end
    
    % 返回最终的门限值
    G = gMin;

end

% =========================================================================
% 计算马氏距离函数
% =========================================================================
% 功能说明：
%   该函数计算向量 d 相对于协方差矩阵逆 S 的马氏距离
% 
% 输入参数：
%   d - 残差向量 [距离残差, 角度残差, 速度残差]
%   S - 协方差矩阵的逆
% 
% 输出参数：
%   chi2 - 计算得到的马氏距离的平方
% 
% 马氏距离公式：
%   chi2 = d^T * S * d
% =========================================================================

function chi2 = computeMahalanobis(d, S)
    % 计算马氏距离的平方
    % 展开计算以提高效率
    chi2 = d(1) * (d(1)*S(1) + d(2)*S(4) + d(3)*S(7)) ...
          + d(2) * (d(1)*S(2) + d(2)*S(5) + d(3)*S(8)) ...
          + d(3) * (d(1)*S(3) + d(2)*S(6) + d(3)*S(9));
end

