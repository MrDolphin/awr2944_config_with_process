% =========================================================================
% 跟踪步骤函数
% =========================================================================
% 功能说明：
%   该函数实现了多目标跟踪的完整步骤，包括：
%   1. 更新点云数据：将新的检测结果转换为点云格式
%   2. 预测目标状态：使用EKF预测目标的下一状态
%   3. 关联点云到目标：将检测点云与现有目标进行关联
%   4. 分配新目标：为未关联的点云创建新的跟踪目标
%   5. 更新目标状态：使用关联的点云更新目标状态
% 
% 输入参数：
%   inst - 跟踪器实例，包含当前跟踪状态
%   ranges_m - 距离数据（米），CFAR检测的距离结果
%   azimuths_rad - 角度数据（弧度），CFAR检测的角度结果
%   dopplers_ms - 多普勒速度数据（米/秒），速度估计结果
%   snrs - 信噪比数据，CFAR检测的信噪比结果
% 
% 输出参数：
%   inst - 更新后的跟踪器实例，包含最新的跟踪状态
% 
% 依赖函数：
%   - update_point_cloud: 更新点云数据
%   - ekf_predict: EKF状态预测
%   - associate: 点云与目标关联
%   - allocate: 分配新目标
%   - ekf_update: EKF状态更新
% =========================================================================

function inst = track_step(inst, ranges_m, azimuths_rad, dopplers_ms, snrs)

    % =====================================================================
    % 1. 更新点云数据
    % =====================================================================
    % 将新的检测结果（距离、角度、速度、信噪比）转换为点云格式
    % 每个检测点创建一个点云元素，包含位置、速度和强度信息
    inst = update_point_cloud(inst, azimuths_rad, ranges_m, dopplers_ms, snrs);

    % =====================================================================
    % 2. 预测目标状态
    % =====================================================================
    % 使用扩展卡尔曼滤波器（EKF）预测每个目标的下一状态
    % 基于目标的当前状态和运动模型预测位置和速度
    inst = ekf_predict(inst);

    % =====================================================================
    % 3. 关联点云到目标
    % =====================================================================
    % 使用门限和马氏距离将检测点云与现有跟踪目标进行关联
    % 为每个点云找到最合适的目标
    inst = associate(inst);

    % =====================================================================
    % 4. 分配新目标
    % =====================================================================
    % 为未关联的点云创建新的跟踪目标
    % 只有当未关联点云的数量超过阈值时才创建新目标
    inst = allocate(inst);

    % =====================================================================
    % 5. 更新目标状态
    % =====================================================================
    % 使用关联的点云更新目标状态
    % 对于有有效关联的目标，使用EKF更新其状态
    % 对于无关联的目标，进行状态管理（如标记为自由状态）
    inst = ekf_update(inst);

end